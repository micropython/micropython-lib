class BMI270:
    def __init__(
        self,
        bus,
        cs=None,
        address=_DEFAULT_ADDR,
        gyro_odr=100,
        gyro_scale=2000,
        accel_odr=100,
        accel_scale=4,
        bmm_magnet=None,
    ):
        """Initalizes Gyro and Accelerometer.
        bus: IMU bus
        address: I2C address (in I2C mode).
        cs: SPI CS pin (in SPI mode).
        gyro_odr:  (0.78, 1.5Hz, 3.1Hz, 6.25Hz, 12.5Hz, 25Hz, 50Hz, 100Hz, 200Hz, 400Hz, 800Hz, 1600Hz)
        gyro_scale:  (125dps, 250dps, 500dps, 1000dps, 2000dps)
        accel_odr: (0.78, 1.5Hz, 3.1Hz, 6.25Hz, 12.5Hz, 25Hz, 50Hz, 100Hz, 200Hz, 400Hz, 800Hz, 1600Hz)
        accel_scale: (+/-2g, +/-4g, +/-8g, +-16g)
        """
        self.bus = bus
        self.bmm_magnet = bmm_magnet
        self.cs = cs
        self.address = address
        self._use_i2c = hasattr(self.bus, "readfrom_mem")

        ACCEL_SCALE = (2, 4, 8, 16)
        GYRO_SCALE = (2000, 1000, 500, 250, 125)
        ODR = (0.78, 1.5, 3.1, 6.25, 12.5, 25, 50, 100, 200, 400, 800, 1200)

        # Sanity checks
        if not self._use_i2c:
            raise ValueError("SPI mode is not supported")
        if gyro_odr not in ODR:
            raise ValueError("Invalid gyro sampling rate: %d" % gyro_odr)
        if gyro_scale not in GYRO_SCALE:
            raise ValueError("Invalid gyro scaling: %d" % gyro_scale)
        if accel_odr not in ODR:
            raise ValueError("Invalid accelerometer sampling rate: %d" % accel_odr)
        if accel_scale not in ACCEL_SCALE:
            raise ValueError("Invalid accelerometer scaling: %d" % accel_scale)
        if self._read_reg(_CHIP_ID) != 0x24:
            raise OSError("No BMI270 device was found at address 0x%x" % (self.address))

        # Perform initialization sequence.
        # 0. Soft-reset
        self._write_reg(_CMD, 0xB6)
        time.sleep_ms(250)

        # 1. Disable power save mode.
        self._write_reg(0x7C, 0x00)
        time.sleep_ms(10)

        # 2. Prepare config load.
        self._write_reg(0x59, 0x00)

        # 3. Load config data.
        self._write_burst(0x5E, _CONFIG_DATA)

        # 4. Finish config load.
        self._write_reg(0x59, 0x01)

        # 5. Check correct initialization status.
        if not self._poll_reg(_STATUS, 0x01):
            raise OSError("Init sequence failed")

        # 6. Configure the device in normal power mode
        # FIFO Reset
        self._write_reg(_CMD, 0xB0)
        # Enable accel, gyro and temperature data.
        self._write_reg(0x7D, 0x0E)
        # acc_filter_perf | acc_bwp normal mode | ODR
        self._write_reg(0x40, 0xA | (ODR.index(accel_odr) + 1))
        # gyr_filter_perf | gyr_bwp normal mode | ODR
        self._write_reg(0x42, 0xA | (ODR.index(gyro_odr) + 1))
        # Disable adv_power_save | Enable fifo_self_wakeup.
        self._write_reg(0x7C, 0x02)

        # Set accelerometer scale and range.
        self.accel_scale = 32768 / accel_scale
        self._write_reg(0x41, ACCEL_SCALE.index(accel_scale))

        # Set gyroscope scale and range.
        self.gyro_scale = 32768 / gyro_scale
        self._write_reg(0x43, GYRO_SCALE.index(gyro_scale))

        # Allocate scratch buffer and set scale.
        self.scratch = memoryview(array.array("h", [0, 0, 0]))

    def _read_reg(self, reg, size=1):
        buf = self.bus.readfrom_mem(self.address, reg, size)
        if size == 1:
            return int(buf[0])
        return buf

    def _read_reg_into(self, reg, buf):
        self.bus.readfrom_mem_into(self.address, reg, buf)

    def _write_reg(self, reg, val):
        if isinstance(val, int):
            val = bytes([val])
        self.bus.writeto_mem(self.address, reg, val)
        time.sleep_ms(1)

    def _write_burst(self, reg, data, chunk=16):
        self._write_reg(_INIT_ADDR_0, 0)
        self._write_reg(_INIT_ADDR_1, 0)
        for i in range(len(data) // chunk):
            offs = i * chunk
            self._write_reg(reg, data[offs : offs + chunk])
            init_addr = ((i + 1) * chunk) // 2
            self._write_reg(_INIT_ADDR_0, (init_addr & 0x0F))
            self._write_reg(_INIT_ADDR_1, (init_addr >> 4) & 0xFF)

    def _poll_reg(self, reg, mask, retry=10, delay=100):
        for i in range(retry):
            if self._read_reg(reg) & mask:
                return True
            time.sleep_ms(delay)
        return False

    def reset(self):
        self._write_reg(_CMD, 0xB6)

    def gyro(self):
        """Returns gyroscope vector in degrees/sec."""
        f = self.gyro_scale
        self._read_reg_into(_DATA_14, self.scratch)
        return (self.scratch[0] / f, self.scratch[1] / f, self.scratch[2] / f)

    def accel(self):
        """Returns acceleration vector in gravity units (9.81m/s^2)."""
        f = self.accel_scale
        self._read_reg_into(_DATA_8, self.scratch)
        return (self.scratch[0] / f, self.scratch[1] / f, self.scratch[2] / f)

    def magnet(self):
        """Returns magnetometer vector."""
        if self.bmm_magnet is not None:
            return self.bmm_magnet.magnet()
        return (0.0, 0.0, 0.0)
